public class TestBase {


	public static WebDriver driver ;
	String screenshotname;
	String timeStamp = new SimpleDateFormat("dd/MM/yyyy").format(Calendar.getInstance().getTime());

	
	public static String downloadPath = System.getProperty("user.dir") + "\\Downloads";

	public static FirefoxOptions firefoxOption() {
		FirefoxOptions option = new FirefoxOptions();
		option.addPreference("browser.download.folderList", 2);
		option.addPreference("browser.download.dir", downloadPath);
		option.addPreference("browser.helperApps.neverAsk.saveToDisk", "application/pdf");
		option.addPreference("browser.download.manager.showWhenStarting", false);
		option.addPreference("pdfjs.disabled", true);
		return option;
	}

	public static ChromeOptions chromeOption() {
		ChromeOptions options = new ChromeOptions();
		HashMap<String, Object> chromePrefs = new HashMap<String, Object>();
		chromePrefs.put("profile.default.content_settings.popups", 0);
		chromePrefs.put("download.default_directory", downloadPath);
		options.setExperimentalOption("prefs", chromePrefs);
		options.setCapability(CapabilityType.ACCEPT_SSL_CERTS, true);
		return options;
	}

	@BeforeClass
	@Parameters({"browser"})
	public void startDriver(@Optional("chrome") String browserName) 
	{
		if (browserName.equalsIgnoreCase("chrome")) {
			System.setProperty("webdriver.chrome.driver", System.getProperty("user.dir")+"/drivers/chromedriver.exe");
			driver = new ChromeDriver(chromeOption()); 
		}

		else if(browserName.equalsIgnoreCase("firefox")) {
			System.setProperty("webdriver.gecko.driver", System.getProperty("user.dir")+"/drivers/geckodriver.exe");
			driver = new FirefoxDriver(firefoxOption()); 
		}

		else if (browserName.equalsIgnoreCase("ie")) 
		{
			System.setProperty("webdriver.ie.driver", System.getProperty("user.dir")+"/drivers/IEDriverServer.exe");
			driver = new InternetExplorerDriver(); 
		}

		else if (browserName.equalsIgnoreCase("safari")) {
			driver = new SafariDriver(); 
		}
		driver.manage().window().maximize();
		driver.manage().timeouts().implicitlyWait(120, TimeUnit.SECONDS);
		driver.navigate().to("https://login.yahoo.com/");
	
		try {
			Thread.sleep(6000);} catch (InterruptedException e) {}
	} 

	@AfterClass
	public void stopDriver() 
	{
		driver.quit();
	}

	// take screenshot when test case success and add it in the Screenshot folder
	@AfterMethod
	public void tsakescreenshoots (ITestResult result  )
	{
		if (result.getStatus()== ITestResult.SUCCESS) 
		{
			
			try {
				Thread.sleep(3000);
			} catch (InterruptedException e) {
				
			}
			
			Path dest = Paths.get(".\\screenshots",result.getName()+".png"); 
			try {
				java.nio.file.Files.createDirectories(dest.getParent()) ;
				FileOutputStream out = new FileOutputStream(dest.toString());
				out.write(((TakesScreenshot) driver).getScreenshotAs(OutputType.BYTES));
				out.close();
			} catch (IOException e) {
				System.out.println("Excpetion while taking screenshot"+ e.getMessage());
			} 
			
			
			System.out.print("thank you amr ");
		}
	
	}
}

